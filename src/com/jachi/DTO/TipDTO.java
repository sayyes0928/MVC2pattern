package com.jachi.DTO;

public class TipDTO {
    String tip_nickname;
    String tip_post;
    int tip_num;
    String tip_title;
    String tip_img;
    
	public String getTip_nickname() {
		return tip_nickname;
	}
	public void setTip_nickname(String tip_nickname) {
		this.tip_nickname = tip_nickname;
	}
	public String getTip_post() {
		return tip_post;
	}
	public void setTip_post(String tip_post) {
		this.tip_post = tip_post;
	}
	public int getTip_num() {
		return tip_num;
	}
	public void setTip_num(int tip_num) {
		this.tip_num = tip_num;
	}
	public String getTip_title() {
		return tip_title;
	}
	public void setTip_title(String tip_title) {
		this.tip_title = tip_title;
	}

	public String getTip_img() {
		return tip_img;
	}
	public void setTip_img(String tip_img) {
		this.tip_img = tip_img;
	}
    
    
}
